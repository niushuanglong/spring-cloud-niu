package com.niu.study.utils;

public class WeekValObj {
    private int year;
    private int week;
    public WeekValObj(int year, int week) {
        super();
        this.year = year;
        this.week = week;
    }
    public int getYear() {
        return year;
    }
    public int getWeek() {
        return week;
    }

}
